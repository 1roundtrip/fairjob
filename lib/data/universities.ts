export function getUniversitySources() {
  return ELITE_UNIVERSITIES.map((u) => ({
    name: u.name + " 就业网",
    url: u.url,
    type: "UNIVERSITY" as SourceType,
    parserType: "UNIVERSAL" as ParserType,
    crawlInterval: 360,
  }));
}

export function getVocationalSources() {
  return VOCATIONAL_COLLEGES.map((c) => ({
    name: c.name,
    url: c.url,
    type: "VOCATIONAL" as SourceType,
    parserType: "UNIVERSAL" as ParserType,
    crawlInterval: 360,
  }));
}

export function getAllUniversitySources() {
  return [...getUniversitySources(), ...getVocationalSources()];
}

